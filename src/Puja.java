public class Puja {
    private double dinero;
    private Usuario usuario;

    public Puja(double dinero, Usuario usuario) {
        this.dinero = dinero;
        this.usuario = usuario;
    }

    public double getDinero() {
        return dinero;
    }

    public Usuario getUsuario() {
        return usuario;
    }
    
    
}
